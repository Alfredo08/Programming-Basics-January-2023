// Loop that prints Hello World 10 times using 'for'
                          // i ++
for( var i = 1; i <= 10; i ++ ){
    console.log( "Hello World!!!", i );
}

// Loop that prings Hello World 10 times using 'while'
var j = 1;
while( j <= 10 ){
    console.log( "Hello World!!!", j );
    j = j + 1;
}


